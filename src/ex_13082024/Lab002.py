# Max Number
num1 = int(input("Enter the first number"))
num2 = int(input("Enter the second number"))
print(max(num1,num2))

# Power
print(pow(num1,num2))

# Addition
sum = num1+num2
print(f"{sum:.3f}")

# Subtraction
sub = num1-num2
print(f"{sub:.3f}")

# Division
div = num1/num2
print(f"{div:.3f}")

# multiplication
mul = num1*num2
print(f"{mul:.3f}")

